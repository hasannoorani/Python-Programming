a = "Hasan is a student \nbut not a teacher"
print(a)

b = "Hasan is a student but not a \"teacher\""
print(b)

c = "\tHasan is a student but not a teacher"
print(c)

a = "Hasan is a student \bbut not a teacher"
print(a)

# escape sequence :-
# \n is escape sequence for new line
# \t is escape sequence for tab
# \b is escape sequence for backspace
# \r is escape sequence for carriage return
# \f is escape sequence for form feed
# \v is escape sequence for vertical tab
# \\ is escape sequence for backslash


