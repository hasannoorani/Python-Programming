# type() function and TypeCasting
# type function is used to find the data type of a given variable in python.
# TypeCasting is the process of converting a variable from one data type to another.

a = 31
t = type(a)  # class <int>
print(t)

b = "31"
t = type(b)
print(b)

i = int("54")
print(i)

f = float(42)
print(f)

s = str(31)
print(s)